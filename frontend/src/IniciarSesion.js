import React, { useState } from "react";
import { useNavigate } from "react-router-dom";
import "./IniciarSesion.css";

function IniciarSesion() {
  const [username, setUsername] = useState("");
  const [password, setPassword] = useState("");
  const [mensaje, setMensaje] = useState("");
  const navigate = useNavigate(); // Para redirigir

  const handleSubmit = async (e) => {
    e.preventDefault();
    setMensaje(""); // limpiar mensaje

    try {
      const response = await fetch("http://localhost:5000/login", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        credentials: "include", // üî• mantiene cookies si usas sesi√≥n
        body: JSON.stringify({ username, password }),
      });

      const result = await response.json();

      if (response.ok && (result.success || result.mensaje === "OK")) {
        // guardar token si viene del backend
        if (result.token) {
          localStorage.setItem("token", result.token);
        }
        setMensaje("‚úÖ Inicio de sesi√≥n exitoso");
        navigate("/"); // redirige al home
      } else {
        setMensaje("‚ö†Ô∏è " + (result.message || result.error || "Usuario o contrase√±a incorrectos"));
      }
    } catch (error) {
      console.error("‚ùå Error:", error);
      setMensaje("‚ùå Error en la conexi√≥n con el servidor.");
    }
  };

  return (
    <div className="login-page">
      <div className="login-container">
        {/* Logo + Texto */}
        <div className="login-side login-logo">
          <h2>Double Œ†</h2>
          <img src="/LOGO.png" alt="Logo" />
        </div>

        {/* Formulario */}
        <div className="login-side login-form">
          <h2>Iniciar Sesi√≥n</h2>

          {mensaje && (
            <p
              style={{
                textAlign: "center",
                color: mensaje.includes("‚úÖ") ? "green" : "red",
                fontWeight: "bold",
              }}
            >
              {mensaje}
            </p>
          )}

          <form onSubmit={handleSubmit}>
            <div className="input-group">
              <label>Digite su correo o username:</label>
              <input
                type="text"
                name="username"
                required
                value={username}
                onChange={(e) => setUsername(e.target.value)}
              />
            </div>

            <div className="input-group">
              <label>Digite su contrase√±a:</label>
              <input
                type="password"
                name="password"
                required
                minLength={3}
                maxLength={10}
                value={password}
                onChange={(e) => setPassword(e.target.value)}
              />
            </div>

            <div className="forgot-password">
              <a href="/recuperar-contrasena">¬øOlvidaste tu contrase√±a?</a>
            </div>

            <button type="submit" className="login-button">
              Entrar
            </button>
          </form>

          <div className="register-section">
            <p>
              ¬øNo tienes cuenta? <a href="/register">Reg√≠strate</a>
            </p>
            <button className="gmail-button">Continuar con Gmail</button>
          </div>
        </div>
      </div>
    </div>
  );
}

export default IniciarSesion;

